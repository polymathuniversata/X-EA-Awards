const fs = require('fs');
const path = require('path');

// Create public directory if it doesn't exist
const publicDir = path.join(__dirname, '..', 'public');
if (!fs.existsSync(publicDir)) {
  fs.mkdirSync(publicDir);
}

// Create a simple favicon.ico (1x1 pixel transparent ICO)
const faviconData = Buffer.from([
  0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x01, 0x01,
  0x00, 0x00, 0x01, 0x00, 0x18, 0x00, 0x0B, 0x00,
  0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x89, 0x50,
  0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00
]);

fs.writeFileSync(path.join(publicDir, 'favicon.ico'), faviconData);

// Create placeholder PNG files
const sizes = [192, 512];
sizes.forEach(size => {
  const pngData = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52
  ]);
  fs.writeFileSync(path.join(publicDir, `logo${size}.png`), pngData);
}); 